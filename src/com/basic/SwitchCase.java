package com.basic;

public class SwitchCase {
public static void main(String s[]) {
	String myName="Arun";
	int myNum=2;
	switch(myName)
	{
	    case "Arun":
		    System.out.println(myName);
		    break;
	    case "Kumar":
	    	System.out.println("Kumar");
	    	break;
	      	
	}
	System.out.println("My number:"+myNum);
}
}
